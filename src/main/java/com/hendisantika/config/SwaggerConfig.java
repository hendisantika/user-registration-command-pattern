@Configuration
@EnableSwagger2
public class SwaggerConfig {

    @Bean
    public Docket apiDocket() {
        return new Docket(DocumentationType.SWAGGER_2)
                .select()
                .apis(RequestHandlerSelectors.basePackage("org.domain.user"))
                .paths(PathSelectors.any())
                .build()
                .apiInfo(getApiInfo());
    }

    private ApiInfo getApiInfo() {
        return new ApiInfo(
                "User Management",
                "Handle the user management activities",
                "1.0.0",
                "Terms of Service",
                new Contact("Hendi santika", "hendisantika.com", "hendisantika@yahoo.co.id"),
                "",
                "",
                Collections.emptyList());
    }
}
